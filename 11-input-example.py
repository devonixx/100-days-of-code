# This is an example of an input function

num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))

print("Sum of", num1, "and", num2, "is", num1 + num2)
print("Difference of", num1, "and", num2, "is", num1 - num2)
print("Product of", num1, "and", num2, "is", num1 * num2)
print("Quotient of", num1, "and", num2, "is", num1 / num2)
print("Remainder of", num1, "and", num2, "is", num1 % num2)
print("Exponentiation of", num1, "and", num2, "is", num1 ** num2)


# This is an example of a len function
numOfLetters = len(input("Enter a word: "))
print("Number of letters in the word is", numOfLetters)
